// Loop from 0 to 4
for (let i = 0; i < 5; i++) {
    console.log(i); // Print the current value of i
}

// Initialize sum
let sum = 0;

// Loop from 1 to 5
for (let i = 1; i <= 5; i++) {
    sum += i; // Add i to sum
}

console.log(sum); // Output the total sum

// Define an array
const fruits = ['apple', 'banana', 'cherry'];

// Loop through each fruit in the array
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]); // Print each fruit
}

// Loop from 0 to 10 with step of 2
for (let i = 0; i <= 10; i += 2) {
    console.log(i); // Print the current value of i
}

// Loop from 5 to 1
for (let i = 5; i >= 1; i--) {
    console.log(i); // Print the current value of i
}
